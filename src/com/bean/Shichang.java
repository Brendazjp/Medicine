package com.bean;

public class Shichang {
	private int id;
	private String shichangid;   //市场编号
	private String diqu;         //所属地区
	private String chengshi;     //所在城市
	private String jingji;       //经济情况
	private String jieri;        //当地的重要节日
	private String funame;       //该市场的负责人姓名
	private String fulianxi;     //负责人的联系电话
	private String celue;        //在该市场的营销策略
	private String beizhu;       //备注
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getShichangid() {
		return shichangid;
	}
	public void setShichangid(String shichangid) {
		this.shichangid = shichangid;
	}
	public String getDiqu() {
		return diqu;
	}
	public void setDiqu(String diqu) {
		this.diqu = diqu;
	}
	public String getChengshi() {
		return chengshi;
	}
	public void setChengshi(String chengshi) {
		this.chengshi = chengshi;
	}
	public String getJingji() {
		return jingji;
	}
	public void setJingji(String jingji) {
		this.jingji = jingji;
	}
	public String getJieri() {
		return jieri;
	}
	public void setJieri(String jieri) {
		this.jieri = jieri;
	}
	public String getFuname() {
		return funame;
	}
	public void setFuname(String funame) {
		this.funame = funame;
	}
	public String getFulianxi() {
		return fulianxi;
	}
	public void setFulianxi(String fulianxi) {
		this.fulianxi = fulianxi;
	}
	public String getCelue() {
		return celue;
	}
	public void setCelue(String celue) {
		this.celue = celue;
	}
	public String getBeizhu() {
		return beizhu;
	}
	public void setBeizhu(String beizhu) {
		this.beizhu = beizhu;
	}
	

}
