package com.bean;

public class Cangku {
	private int id;
	private String ckname;    //仓库名称
	private String dizhi;     //仓库地址
	private String mianji;    //仓库面积
	private String shitime;   //开始使用时间
	private String chanlei;   //主放产品类型
	private String ckphone;   //仓库电话
	private String fuzeren;   //仓库负责人
	private String fuphone;   //负责人电话
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getCkname() {
		return ckname;
	}
	public void setCkname(String ckname) {
		this.ckname = ckname;
	}
	public String getDizhi() {
		return dizhi;
	}
	public void setDizhi(String dizhi) {
		this.dizhi = dizhi;
	}
	public String getMianji() {
		return mianji;
	}
	public void setMianji(String mianji) {
		this.mianji = mianji;
	}
	public String getShitime() {
		return shitime;
	}
	public void setShitime(String shitime) {
		this.shitime = shitime;
	}
	public String getChanlei() {
		return chanlei;
	}
	public void setChanlei(String chanlei) {
		this.chanlei = chanlei;
	}
	public String getCkphone() {
		return ckphone;
	}
	public void setCkphone(String ckphone) {
		this.ckphone = ckphone;
	}
	public String getFuzeren() {
		return fuzeren;
	}
	public void setFuzeren(String fuzeren) {
		this.fuzeren = fuzeren;
	}
	public String getFuphone() {
		return fuphone;
	}
	public void setFuphone(String fuphone) {
		this.fuphone = fuphone;
	}

}
